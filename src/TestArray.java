public class TestArray {
    public static void main(String[] args) {
        //creates and initialise double array numbers with a size of 4
        double[] myList = {1.9, 2.9, 3.4, 3.5};

        //for loop to iterate over the elements in myList array, and incrementing i++
        for (int i = 0; i < myList.length; i++) {
            System.out.println(myList[i] + " "); // Print output all the array elements
        }
    }
}